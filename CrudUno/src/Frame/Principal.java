    /**
     * Método que se ejecuta cuando se hace clic en el botón "Guardar". Realiza
     * una inserción de datos en la base de datos empleadoss.
     *
     * @param evt Evento de acción del botón.
     */
    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed
        try {
            // Consulta SQL para insertar un nuevo registro en la tabla empleadoss
            String query = "INSERT INTO empleadoss(nombre, apellidos, direccion, telefono) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = con.getConexion().prepareStatement(query);

            // Establece los valores de los parámetros de la consulta
            ps.setString(1, txtnombre.getText());
            ps.setString(2, txtapellidos.getText());
            ps.setString(3, txtdireccion.getText());
            ps.setString(4, txttelefono.getText());

            // Ejecuta la inserción
            ps.executeUpdate();

            // Muestra un mensaje de éxito
            JOptionPane.showMessageDialog(rootPane, "Registro insertado correctamente");

            // Actualiza la visualización de la tabla
            mostrartabla("");
            ps.close();

        } catch (SQLException e) {
            // En caso de error, muestra un mensaje de error con el detalle
            JOptionPane.showMessageDialog(rootPane, "Error al ejecutar la consulta: " + e.getMessage());
            System.err.println();
        }

    }//GEN-LAST:event_btguardarActionPerformed

