    private void btactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btactualizarActionPerformed
         try {
            // Consulta SQL para insertar un nuevo registro en la tabla empleadoss
            String query = "UPDATE empleadoss SET nombre = ?, apellidos = ?, direccion = ?, telefono = ? WHERE id = ?";

            PreparedStatement ps = con.getConexion().prepareStatement(query);
            
            ps.setString(1, txtnombre.getText());
            ps.setString(2, txtapellidos.getText());
            ps.setString(3, txtdireccion.getText());
            ps.setString(4, txttelefono.getText());
            ps.setString(5, txtid.getText());

            int filasAfectadas = ps.executeUpdate();
             if (filasAfectadas > 0) {
                 JOptionPane.showMessageDialog(rootPane, " actualizacion realizada de forma correcta ");
                 System.out.println("");
                 limpiar();
                 mostrartabla("");
                 
             }else{
                 JOptionPane.showMessageDialog(rootPane,"no se encontro empleado con ID");
                 System.out.println("");
             }
   
           ps.close();

          
           

        } catch (SQLException e) {
            // En caso de error, muestra un mensaje de error con el detalle
            JOptionPane.showMessageDialog(rootPane, "Error al ejecutar la consulta: " + e.getMessage());
            System.err.println();
        }
    }//GEN-LAST:event_btactualizarActionPerformed

